print("執行結果")
I = float(input("請輸入當月利潤,單位為萬元:"))
bns = 0  
cat = [100, 60, 40, 20, 10, 0]  
pct = [0.01, 0.015, 0.03, 0.05, 0.075, 0.1]  
for i in range(6):
    if I > cat[i]:
        I = I - cat[i]  
        bns = bns + I * pct[i]  
print('當月應發放奬金總數為%s萬元' % bns)
